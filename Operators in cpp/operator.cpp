/*
Operators in C++

--> Arithmetic
--> Relational
--> Logical

 */

 #include <iostream>
 using namespace std;
 
 int main()
 {
    int a = 5;
    int b = 2;
    int c = a / b;
    cout << c << endl;

    int _a = 5;
    float _b = 2;
    float _c = _a / _b;
    cout << _c;
    return 0;
 }

 //     int / int --> int
 //     int / float --> float